class arr{
    public static void main(String args[]){
        int a[] = {1,2,3,4,5};
        int length = a.length;
        System.out.print("Length of arr is "+length+"\nElements are : ");
        for(int i=0;i<length;i++){
            System.out.print(a[i]+" ");
        }
        System.out.println();
    }
}